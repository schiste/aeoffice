export function createRpgIdleDemoMap(): RpgIdleDemoMap {
  const definition = rpgIdleDemoDefinition()
  const compiled = compileSemanticMapDefinition(definition, RPG_IDLE_DEMO_CATALOG)
  const spawnPoints = [
    tileSpawnPoint("hero_spawn", { x: 4, y: 4 }, compiled.tileSize),
  ]
  const validation = validateCompiledMap(compiled, spawnPoints)

  return {
    id: "rpg_idle_grove",
    label: "Grove Outpost",
    definition,
    compiled,
    spawnPoints,
    validation,
  }
}

