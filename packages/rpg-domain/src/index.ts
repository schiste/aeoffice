export function referencedRpgTokens(
  compiled: CompiledSemanticMap,
): readonly VisualTokenDefinition[] {
  const referencedTokenIds = new Set(compiled.referencedTokenIds)
  return RPG_IDLE_DEMO_CATALOG.tokens.filter((token) =>
    referencedTokenIds.has(token.id),
  )
}

function boundaryWalls(
  width: number,
  height: number,
): SemanticMapDefinition["layers"]["walls"] {
  const walls: SemanticWallPlacement[] = []

  for (let x = 0; x < width; x += 1) {
    walls.push({ x, y: 0, type: isHorizontalCorner(x, width) ? "corner" : "straight" })
    walls.push({
      x,
      y: height - 1,
      type: isHorizontalCorner(x, width) ? "corner" : "straight",
    })
  }

  for (let y = 1; y < height - 1; y += 1) {
    walls.push({ x: 0, y, type: "straight" })
    walls.push({ x: width - 1, y, type: "straight" })
  }

  return walls
}

function isHorizontalCorner(x: number, width: number): boolean {
  return x === 0 || x === width - 1
}

function token(
  id: string,
  kind: VisualTokenDefinition["kind"],
  layer: VisualTokenDefinition["layer"],
  provisionalGid: number,
  widthTiles: number,
  heightTiles: number,
  collidable: boolean,
  tags: readonly string[],
  options: FrameOptions = {},
): VisualTokenDefinition {
  return {
    id,
    kind,
    layer,
    sourceId: SOURCE_ID,
    tilesetId: TILESET_ID,
    provisionalGid,
    widthTiles,
    heightTiles,
    collidable,
    asset: frame(id, widthTiles, heightTiles, collidable, options),
    tags,
  }
}

function frame(
  frameId: string,
  widthTiles: number,
  heightTiles: number,
  collidable: boolean,
  options: FrameOptions,
): VisualAssetFrameMetadata {
  const width = widthTiles * TILE_SIZE
  const height = heightTiles * TILE_SIZE

  return {
    atlasId: ATLAS_ID,
    frameId,
    size: {
      width,
      height,
      exportScale: 1,
    },
    anchor: {
      x: width / 2,
      y: height / 2,
    },
    collisionFootprint:
      options.collisionFootprint ??
      (collidable
        ? { x: 0, y: 0, width, height }
        : { x: 0, y: 0, width: 0, height: 0 }),
    visualFootprint: options.visualFootprint ?? { x: 0, y: 0, width, height },
    shadowFootprint:
      options.shadowFootprint ??
      (frameId.startsWith("floor.")
        ? { x: 0, y: 0, width: 0, height: 0 }
        : {
            x: Math.round(width * 0.16),
            y: Math.max(0, height - 7),
            width: Math.round(width * 0.68),
            height: 5,
          }),
    zAnchor: options.zAnchor ?? { x: width / 2, y: height },
    interaction: options.interaction ?? { affordance: "none" },
    occlusion: {
      mode: options.occlusionMode ?? "none",
    },
    themeTags: options.themeTags ?? ["rpg", "procedural"],
    variants: [
      {
        id: `${frameId}.default`,
        label: "Default",
        role: "default",
        frameId,
        themeTags: options.themeTags ?? ["rpg", "procedural"],
      },
    ],
  }
}
